import React from 'react'
import ReactDOM from 'react-dom/client'
import MorganOS from './App.jsx'
import './index.css'

ReactDOM.createRoot(document.getElementById('root')).render(
  <React.StrictMode>
    <MorganOS />
  </React.StrictMode>,
)

// Register service worker for PWA
